		  RTC_Read(rtc_data);
		  char lcd_msg[17];
		  snprintf(lcd_msg, sizeof(lcd_msg), "%02d:%02d %2.1f %d", rtc_data[2], rtc_data[1], bmp_temp, bmp_pres);
		  LCD_Clear();
		  LCD_Print(lcd_msg);
		  snprintf(lcd_msg, sizeof(lcd_msg), "DHT: %-2.0f %-2.0f%%", dht_data[0], dht_data[1]);
		  LCD_SetCursor(1, 0);
		  LCD_Print(lcd_msg);
		  snprintf(lcd_msg, sizeof(lcd_msg), "%3ldm\0", bmp_alt);
		  LCD_SetCursor(1, 12);
		  LCD_Print(lcd_msg);

